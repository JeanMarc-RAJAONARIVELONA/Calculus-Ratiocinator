package com.Calculusratiocinator;

public class LogicUtils {
    public static boolean AndLogic(boolean a, boolean b) {
        return a && b;
    }

    public static boolean OrLogic(boolean a, boolean b) {
        return a || b;
    }

    public static boolean NotLogic(boolean a, boolean b) {
        return !a && b;
    }
}
